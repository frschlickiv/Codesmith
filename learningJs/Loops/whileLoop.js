let i = 0; 
while (i<= 5) {
    if (i % 2 !== 0 ) console.log(i);
    i++;
}


//do while loop (notice how the while portion is at the end of the loop? This means the if statement
//will always run at least once in a do while statement)
let i = 0;
do {
if (i % 2 !==0) console.log(i);
i++;
} while (i<=5);



